def wilson_loop_nonabelian(p1, p2):
    """Non-Abelian Wilson loop for walk by p1 then p2 then back."""
    sp1 = (p1 // 6) % 6
    sp2 = (p2 // 6) % 6
    r1 = "R1" if p1 % 6 == 5 else "R2"
    r2 = "R1" if p2 % 6 == 5 else "R2"

